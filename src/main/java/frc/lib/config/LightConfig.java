package frc.lib.config;

public class LightConfig {
    public final int blinkin1;
    public final int blinkin2;

    public final double color1;
    public final double color2;

    public LightConfig(int blinkin1, int blinkin2, double color1, double color2) {
        this.blinkin1 = blinkin1;
        this.blinkin2 = blinkin2;
        this.color1 = color1;
        this.color2 = color2;
    }
}
